public class AppParameters {
    String userKey;
    byte[] ATU;
    String hatu;

    public String getUserKey() {
        return userKey;
    }

    public void setUserKey(String userKey) {
        this.userKey = userKey;
    }

    public byte[] getATU() {
        return ATU;
    }

    public void setATU(byte[] ATU) {
        this.ATU = ATU;
    }

    public String getHatu() {
        return hatu;
    }

    public void setHatu(String hatu) {
        this.hatu = hatu;
    }
}
